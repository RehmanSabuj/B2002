package java_string_demo;

public class StringDemo {
    public static void main(String[] args) {

        String name = "John";
        System.out.println(name);
        String student01 = new String("David Galtier");
        System.out.println(student01);
        System.out.println(student01.length());
        System.out.println(student01.charAt(0));
        System.out.println(student01.indexOf(4));
        System.out.println(student01.charAt(3));
    }

}
